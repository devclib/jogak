import { describe, expect, it, beforeEach, afterEach } from 'vitest'
import { mkdtemp, mkdir, readFile, rm, writeFile } from 'node:fs/promises'
import { tmpdir } from 'node:os'
import { join } from 'node:path'
import { generateRegistryFile } from './generate.js'

describe('generateRegistryFile — emit 회귀 보호', () => {
  let dir: string

  beforeEach(async () => {
    dir = await mkdtemp(join(tmpdir(), 'jogak-generate-'))
  })

  afterEach(async () => {
    await rm(dir, { recursive: true, force: true })
  })

  async function setupFixture(): Promise<void> {
    await mkdir(join(dir, 'src/components'), { recursive: true })
    await writeFile(
      join(dir, 'src/components/Hello.tsx'),
      `export const Hello = ({ label }: { label: string }) => <span>{label}</span>\n`,
      'utf8',
    )
    await writeFile(
      join(dir, 'src/components/Hello.jogak.tsx'),
      `import type { JogakMeta, Jogak } from '@jogak/core'
import { Hello } from './Hello'
const meta = { title: 'Components/Hello', component: Hello } satisfies JogakMeta
export default meta
export const Default: Jogak = { name: 'Default', args: { label: 'world' } }
`,
      'utf8',
    )
  }

  it('indexed access에 fallback이 emit되어 strict tsconfig를 통과한다', async () => {
    // noUncheckedIndexedAccess: true 환경에서 _sources[i], _autoArgTypes[i]가
    // string | undefined로 추론되던 결함 (알파.14.2까지) 재발 방지.
    await setupFixture()
    const out = join(dir, '.jogak/registry.ts')
    await generateRegistryFile({
      patterns: ['src/**/*.jogak.tsx'],
      cwd: dir,
      outFile: '.jogak/registry.ts',
    })
    const content = await readFile(out, 'utf8')

    // ✅ 정상: indexed access에 ?? fallback
    expect(content).toMatch(/_sources\[\d+\]\s*\?\?\s*''/u)
    expect(content).toMatch(/_autoArgTypes\[\d+\]\s*\?\?\s*\{\}/u)

    // ❌ 금지: fallback 없는 bare indexed access (strict env에서 fail)
    expect(content).not.toMatch(/_buildEntry\([^)]*_sources\[\d+\]\s*,/u)
    expect(content).not.toMatch(/_buildEntry\([^)]*_autoArgTypes\[\d+\]\)/u)
  })

  it('생성된 파일 골격이 유지된다 (entries / metas export, _buildEntry / _buildMeta 헬퍼)', async () => {
    await setupFixture()
    await generateRegistryFile({
      patterns: ['src/**/*.jogak.tsx'],
      cwd: dir,
      outFile: '.jogak/registry.ts',
    })
    const content = await readFile(join(dir, '.jogak/registry.ts'), 'utf8')

    expect(content).toContain('AUTO-GENERATED by @jogak/core/build')
    expect(content).toContain("import type { RegistryEntry, RegistryEntryMeta, ArgType } from '@jogak/core'")
    expect(content).toContain('function _buildEntry(')
    expect(content).toContain('function _buildMeta(')
    expect(content).toMatch(/export const entries:\s*readonly RegistryEntry\[\]/u)
    expect(content).toMatch(/export const metas:\s*readonly RegistryEntryMeta\[\]/u)
  })

  it('파일 0개여도 정상 emit (빈 entries/metas)', async () => {
    await mkdir(join(dir, 'src'), { recursive: true })
    const result = await generateRegistryFile({
      patterns: ['src/**/*.jogak.tsx'],
      cwd: dir,
      outFile: '.jogak/registry.ts',
    })
    expect(result.fileCount).toBe(0)
    const content = await readFile(join(dir, '.jogak/registry.ts'), 'utf8')
    expect(content).toContain('export const entries')
    expect(content).toContain('export const metas')
  })
})
