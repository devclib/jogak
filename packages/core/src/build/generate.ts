import { glob } from 'glob'
import { existsSync } from 'node:fs'
import { mkdir, writeFile, readFile } from 'node:fs/promises'
import { dirname, relative, resolve } from 'node:path'
import { createPropsExtractor } from '../meta/extract-props.js'
import type { ArgType } from '../types.js'

export interface GenerateOptions {
  readonly patterns: readonly string[]
  readonly cwd: string
  /** outFile은 cwd에 대한 상대 경로 또는 절대 경로 */
  readonly outFile: string
  readonly tsConfigFilePath?: string
}

export interface GenerateResult {
  readonly fileCount: number
  readonly outFile: string
}

/**
 * `*.jogak.tsx` 파일들을 스캔해 `entries`를 export하는 일반 TS 파일을 생성한다.
 *
 * Vite의 가상 모듈(`virtual:jogak`)과 동일한 형태의 데이터를 일반 import 가능한
 * TS 파일로 출력하여 Next.js·Webpack·Turbopack·Rspack 등 어떤 호스트 번들러에서도
 * 별도 플러그인 없이 마운트할 수 있게 한다.
 *
 * - props 자동 추출(ts-morph)은 빌드 타임에 1회 수행
 * - source는 문자열로 inline (Vite의 `?raw` 같은 번들러별 문법 회피)
 */
export async function generateRegistryFile(options: GenerateOptions): Promise<GenerateResult> {
  const cwd = resolve(options.cwd)
  const outFileAbs = resolve(cwd, options.outFile)
  const outDir = dirname(outFileAbs)

  const files = (await glob(options.patterns as string[], { cwd, absolute: true })).sort()

  // 알파.12: tsConfig 자동 감지. 사용자가 명시하지 않아도 cwd/tsconfig.json이 있으면 사용 —
  // 그래야 ts-morph가 cross-file declaration(예: badge.jogak.tsx → badge.tsx)을 해석해
  // JSDoc description/@default가 정확히 추출된다.
  const resolvedTsConfig =
    options.tsConfigFilePath ??
    (existsSync(resolve(cwd, 'tsconfig.json'))
      ? resolve(cwd, 'tsconfig.json')
      : undefined)

  const extractor =
    resolvedTsConfig !== undefined
      ? createPropsExtractor({ tsConfigFilePath: resolvedTsConfig })
      : createPropsExtractor()

  const sources: string[] = []
  const autoArgTypes: Record<string, ArgType>[] = []
  try {
    for (const file of files) {
      sources.push(await readFile(file, 'utf8'))
      try {
        autoArgTypes.push(await extractor.extract(file))
      } catch {
        autoArgTypes.push({})
      }
    }
  } finally {
    // 추출 끝나면 자식 프로세스를 즉시 종료해 CLI가 매달리지 않도록.
    // (idle 5초 타이머가 unref이지만, 명시 dispose가 더 결정적.)
    extractor.releaseCache()
  }

  const importLines = files
    .map((file, i) => {
      const rel = relative(outDir, file).replace(/\\/g, '/').replace(/\.(tsx?|jsx?)$/u, '')
      const importPath = rel.startsWith('.') ? rel : `./${rel}`
      return (
        `import _meta${i.toString()} from ${JSON.stringify(importPath)}\n` +
        `import * as _named${i.toString()} from ${JSON.stringify(importPath)}`
      )
    })
    .join('\n')

  const sourceArray = sources.map((s) => JSON.stringify(s)).join(',\n  ')
  const autoArgTypesArray = autoArgTypes.map((a) => JSON.stringify(a)).join(',\n  ')
  const buildCalls = files
    .map(
      (_, i) =>
        // `noUncheckedIndexedAccess: true` 사용자 환경에서도 typecheck를 통과하도록
        // indexed access에 fallback. 인덱스는 항상 유효하므로 런타임 동작은 동일.
        `_buildEntry(_meta${i.toString()}, _named${i.toString()}, _sources[${i.toString()}] ?? '', _autoArgTypes[${i.toString()}] ?? {})`,
    )
    .join(',\n  ')

  const content = `// AUTO-GENERATED by @jogak/core/build — do not edit.
/* eslint-disable */
import type { RegistryEntry, RegistryEntryMeta, ArgType } from '@jogak/core'
${importLines}

const _sources: readonly string[] = [
  ${sourceArray}
]

const _autoArgTypes: ReadonlyArray<Record<string, ArgType>> = [
  ${autoArgTypesArray}
]

function _buildEntry(
  meta: any,
  named: Record<string, unknown>,
  source: string,
  autoArgTypes: Record<string, ArgType>,
): RegistryEntry | null {
  if (meta == null || typeof meta.title !== 'string') return null
  const jogaks = Object.values(named).filter(
    (v): v is { name: string } =>
      v !== null && typeof v === 'object' && typeof (v as { name?: unknown }).name === 'string',
  )
  const userArgTypes: Record<string, ArgType> = meta.argTypes ?? {}
  const mergedArgTypes: Record<string, ArgType> = { ...autoArgTypes }
  for (const key of Object.keys(userArgTypes)) {
    mergedArgTypes[key] = { ...mergedArgTypes[key], ...userArgTypes[key] }
  }
  return {
    id: meta.title,
    title: meta.title,
    jogaks: jogaks as RegistryEntry['jogaks'],
    meta: { ...meta, argTypes: mergedArgTypes },
    source,
  }
}

function _buildMeta(entry: RegistryEntry, autoArgTypes: Record<string, ArgType>): RegistryEntryMeta {
  const userArgTypes = (entry.meta.argTypes ?? {}) as Record<string, ArgType>
  // 알파.14.1: eager build의 metaonly 사이드바도 jogakDefaultArgs를 가져야 한다.
  // build 사이클은 hydrated entry로부터 직접 추출.
  const jogakDefaultArgs: Record<string, Record<string, unknown>> = {}
  for (const j of entry.jogaks) {
    if (j.args !== undefined) {
      jogakDefaultArgs[j.name] = j.args as Record<string, unknown>
    }
  }
  return {
    id: entry.id,
    title: entry.title,
    jogakNames: entry.jogaks.map((j) => j.name),
    jogakDefaultArgs,
    autoArgTypes,
    userArgTypes,
    source: entry.source ?? '',
    filePath: entry.filePath ?? '',
    metaExtras: {
      ...(entry.meta.tags !== undefined ? { tags: entry.meta.tags } : {}),
      ...(entry.meta.parameters !== undefined ? { parameters: entry.meta.parameters } : {}),
    },
    // 알파.14.1: lazy 사이드바 dispatch용 framework 보존. eager build에서는
    // 사용자 meta.framework가 있으면 그 값, 없으면 undefined (호스트 ui가 fallback 결정).
    ...(entry.meta.framework !== undefined ? { framework: entry.meta.framework } : {}),
  }
}

export const entries: readonly RegistryEntry[] = [
  ${buildCalls}
].filter((e): e is RegistryEntry => e !== null)

/**
 * 사이드바 메타 — \`<JogakApp metas={metas} />\` 패턴용.
 * eager 모드에서는 entries로부터 합성 — ts-morph 추가 호출 없음.
 */
export const metas: readonly RegistryEntryMeta[] = entries.map((e, i) =>
  _buildMeta(e, _autoArgTypes[i] ?? {}),
)
`

  await mkdir(outDir, { recursive: true })
  await writeFile(outFileAbs, content, 'utf8')

  return { fileCount: files.length, outFile: outFileAbs }
}
